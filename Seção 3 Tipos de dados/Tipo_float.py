"""
Tipo Float

Tipo real, decimal

Casas decimais

OBS: O separador de casas decimais na programação e o (ponto final)
"""

# Tipo Ponto flutuante(float).

altura = 1.79 
print(altura) 

# E possivel fazer a dupla atribuição
valo1, valor2 = 1, 44
print(valo1)
print(valor2)

# Podemos converter um  float para um int
"""
OBS: Ao converter valores float para inteiros, nos perdemos precisão.
"""
print(int(altura)) # Posso fazer dessa forma, direto
res = int(altura) # Posso fazer dessa forma, criando uma variavel (res) e atribuindo a ela (int) e dentro de parenteses o nome da variavel.
print(res)



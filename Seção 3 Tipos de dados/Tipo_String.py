"""
Tipo string

Em Python, um dado e considerado do tipo string sempre que:

1 - Estiver entre àspas simples -> 'uma string', '234', 'a', 'True', 'Teste'
2 - Estiver entre àspas duplas  -> "uma string", "234", "True", "a", "24.0", 
3 - Estiver entre àspas simples triplas -> '''teste''', '''True''', '''234''', '''23.0'''
"""
# Exemplo:
print('Paulo') # Comprovando 1
print("Gabrielle") # Comprovado 2
print('''Arthur''') # Comprovado 3

